extern void * malloc(int);

char * long_to_string(unsigned long a) {
    int N = 20;
    char buf[N];
    int n = N;

    do {
        unsigned long d = a % 10;
        a = a / 10;

        n --;
        buf[n] = (char)(48 + (int)d);
    } while(a > 0);

    char * ret = malloc(N - n + 1);
    memcpy(ret, buf + n, N - n);
    ret[N - n] = '\0';
    return ret;
}

extern int main () {
    int n = 0;
    for (unsigned long a = 1; n < 64; a *= 2, n ++)
        printf("2^%d = %ld, as string: '%s'\n", n, a, long_to_string(a));

    return 0;
}
