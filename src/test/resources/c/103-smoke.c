const int start = 25;
const int end = 40;
const int base = 3;
long pow2(int n) {
    long res = 1;
    do {
        res = res * 2;
        n = n - 1;
    }
    while(n > 0);

    return res;
}

long pow(int base, int n) {
    long res = 1;
    do {
        res = res * (long)base;
        n = n - 1;
    }
    while(n > 0);

    return res;
}

extern int main() {
    int x = start;
    do {
        printf("2^%d = %ld, (%d)^%d = %ld\n", x, pow2(x), -base, x, pow(-base, x));
        x = x + 1;
    }
    while(x <= end);
    return 0;
}
