static unsigned int seed = 57;

double mulberry32() {
    seed += (unsigned int) 1831565813;
    unsigned int t = seed;
    t = (t ^ t >> (unsigned int)15) * (t | (unsigned int)1);
    t ^= t + (t ^ t >> (unsigned int)7) * (t | (unsigned int)61);
    return (t ^ t >> (unsigned int)14) / 4294967296.0;
}

double array_min_(double arr[], int a, int b) {
    if (b == a)
        return arr[a];
    else if (b == a + 1)
        return min(arr[a], arr[b]);
    else {
        int c = (a + b)/2;
        return min(array_min_(arr, a, c), array_min_(arr, c+1, b));
    }
}

double array_min(double arr[], int N) {
    return array_min_(arr, 0, N - 1);
}

double * make_array(int N) {
    double * arr = (double *) malloc(N * sizeof(double));
    for (int i = 0; i < N; i ++)
        arr[i] = mulberry32();
    return arr;
}

